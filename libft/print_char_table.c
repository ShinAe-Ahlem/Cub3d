#include "libft.h"

void	print_char_table(char **tab)
{
	if (tab)
	{
		while (*tab)
			ft_putstr_fd(*tab++, 1);
		ft_putstr_fd("\n\n ----------------------- seperation end of map ---------------------\n\n", 1);
	}
}
